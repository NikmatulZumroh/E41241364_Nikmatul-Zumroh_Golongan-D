public class For_Each {
    public static void main(String[] args) {
        // Membuat array
        int angka[] = {3,1,42,24,12};
        // menggunakan perulangan for each untuk menampilkan angka
        for (int z : angka){
            System.out.print(z + " ");
        }
    }
}
